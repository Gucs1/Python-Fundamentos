# Função = Existe retorno
# Procedimento = não possui retorno
# Parametro = É a variavel recebida em uma funcao ou procedimento
# Argumento = É o dado passado durante a chamada de uma funcao ou procedimento

#somar
def somar(numero1, numero2):
    resultado = numero1 + numero2
    print(resultado)

#subtrair
def sub(numero1, numero2):
    resultado = numero1 - numero2
    print(resultado)

#multiplicar
def mult(numero1, numero2):
    resultado = numero1 * numero2
    print(resultado)

#dividir
def div(numero1, numero2):
    resultado = numero1 / numero2
    print(resultado)

